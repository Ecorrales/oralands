/** Traduce una clave al idioma actual. Soporta interpolación simple {name}. */
export function t(key: string, vars?: Record<string, string | number>): string {
  const entry = DICT[key];
  let s = entry ? entry[currentLang] : key;
  if (vars) for (const [k, v] of Object.entries(vars)) s = s.replace(`{${k}}`, String(v));
  return s;
}

// Helpers de contenido (leen del mismo diccionario por convención de clave).
export const statLabel = (k: string): string => t("stat." + k);
export const statAbbr  = (k: string): string => t("stat.abbr." + k);
export const statDesc  = (k: string): string => t("stat.desc." + k);
export const slotLabel = (k: string): string => t("slot." + k);
export const abilityName = (id: string): string => { const e = DICT["ability." + id]; return e ? e[currentLang] : id; };
export const abilityDesc = (id: string): string => { const e = DICT["ability.desc." + id]; return e ? e[currentLang] : ""; };

// Traducción de nombres de contenido (armas, enemigos, etc.) por su texto en español.
// Los nombres se guardan como strings en el juego; este índice inverso los traduce al mostrarlos.
let REV: Map<string, string> | null = null;
export function tName(esName: string): string {
  if (currentLang === "es") return esName;
  if (!REV) { REV = new Map(); for (const e of Object.values(DICT)) REV.set(e.es, e.en); }
  return REV.get(esName) ?? esName;
}
