// i18n bilingüe (español ⇄ inglés). El idioma vive como variable de módulo para que
// TODO el código (React y no-React) pueda leerlo vía t(). El botón de idioma cambia
// currentLang y fuerza un re-render del árbol; como t() lee la variable, todo se actualiza.
export type Lang = "es" | "en";

const STORAGE_KEY = "oralands_lang";

function initialLang(): Lang {
  try {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved === "es" || saved === "en") return saved;
  } catch { /* localStorage no disponible */ }
  // por defecto: español si el navegador lo es, inglés si no
  try { return navigator.language?.toLowerCase().startsWith("es") ? "es" : "en"; } catch { return "es"; }
}

let currentLang: Lang = initialLang();

export const getLang = (): Lang => currentLang;
export function setLang(l: Lang): void {
  currentLang = l;
  try { localStorage.setItem(STORAGE_KEY, l); } catch { /* ignore */ }
}

